C$Procedure      DROTAT ( Derivative of a rotation matrix )
 
      SUBROUTINE DROTAT ( ANGLE, IAXIS, DMOUT )
 
C$ Abstract
C
C      Calculate the derivative with respect to the angle of rotation
C      of a 3x3 coordinate system rotation matrix generated by a
C      rotation of a specified angle about a specified axis.
C
